package org.wbsilva.bence.evaluation;

import org.eclipse.emf.ecore.EPackage;

/**
 * An instance of this class hold the specification of a transformation between two metamodels in BeNCE and eMoflon
 * @author wbombardellis
 * Immutable
 */
public class TGGSpecification {
	
	private final EPackage sourcePackage;
	private final EPackage targetPackage;
	private final String benceTGGPath;
	private final EPackage eMoflonTGGPackage;
	private final String eMoflonTGGPath;
	private final boolean forward;
	
	/**
	 * Construct a TGG specification
	 * @param sourcePackage				The ePackage of the source metamodel
	 * @param targetPackage				The ePackage of the target metamodel
	 * @param benceTGGPath				The path to the file containing the TGG in BeNCE
	 * @param eMoflonTGGPackage			The ePackage containing the TGG in eMoflon, generated by eMoflon
	 * @param eMoflonTGGPath			The path to the eMoflon project of this TGG
	 * @param forward					Flag to signalize if transformation is forward or backward
	 */
	public TGGSpecification(final EPackage sourcePackage, final EPackage targetPackage, final String benceTGGPath,  final EPackage eMoflonTGGPackage, final String eMoflonTGGPath, final boolean forward) {
		this.sourcePackage = sourcePackage;
		this.targetPackage = targetPackage;
		this.benceTGGPath = benceTGGPath;
		this.eMoflonTGGPackage = eMoflonTGGPackage;
		this.eMoflonTGGPath = eMoflonTGGPath;
		this.forward = forward;
	}

	public EPackage getSourcePackage() {
		return sourcePackage;
	}

	public EPackage getTargetPackage() {
		return targetPackage;
	}

	public String getBenceTGGPath() {
		return benceTGGPath;
	}

	public EPackage getEMoflonTGGPackage() {
		return eMoflonTGGPackage;
	}
	
	public String getEMoflonTGGPath() {
		return eMoflonTGGPath;
	}

	public boolean getForward() {
		return forward;
	}
}
